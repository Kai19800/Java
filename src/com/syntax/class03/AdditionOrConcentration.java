package com.syntax.class03;

public class AdditionOrConcentration {

	public static void main(String[] args) {
		int a=10;
		short b=500;
		short c=200;
		
		System.out.println(a+=b);
		System.out.println(a-=c);
		
		int g=100;
		int v=60;
		
		System.out.println(g-=v);
		
		int cakepiece=11;
		int people=4;
		System.out.println(cakepiece/=people);
		
		int cake=25;
		
		System.out.println(cake/=7);
		System.out.println(cake%=7);
			
				
				
				
			
				

	}

}
